package Day03_variables;

public class escapeSequences {
	/*
	 
	 
	 \n or \r: where ever put \n it breaks the line(start the new line)(gives line)
	 \t:(horizontal tap) where ever you put it it gives some space
	 \\:prints back slash
	 \': prints single quote
	 \": prints double quote
	 
	 */
	public static void main(String[] args) {
		System.out.println("Hello Cybertek|\nBatch 12 Students");
		System.out.print("Tomorrow is off \n");
		System.out.print("True\n");
		System.out.println("False\n\n\n");
		System.out.println("\tClass starts tomorrow");
		System.out.println("\\filiz\\");
		System.out.println("\'java\'");
		System.out.println("Book is called \"harry potter\"");
		System.out.println("\"hello\"");
		
	}

}
